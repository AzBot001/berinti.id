                <h4>Udah tahu tambang emas ada sejak kapan?</h4>
                <br />
                Aktivitas pertambangan di wilayah Suwawa ini mulai beroperasi sejak
                tahun 90-an. Lokasi yang pertama kali dilakukan penggalian yaitu
                titik bor 17 pada tahun 1992. Kemudian dua tahun berikutnya para
                penambang melakukan penggalian kembali dan menemukan lubang yang
                begitu banyak emas, tepatnya berada di titik bor 1. "Dulu itu yang
                pertama kali dilakukan penggalian di titik bor 17 tahun 1992, nanti
                di tahun 1994 titik bor 1 ini baru ada aktivitas tambang," jelas
                Leon yang mulai menambang sejak 1996 silam. Cerita Leon, pada tahun
                90-an, terdapat sebuah perusahaan yang bernama Tropik. Perusahaan
                ini yang pertama kali menambang di tambang emas Suwawa.

                <br><br>Adapun
                penggunaan nama titik bor di tambang ilegal Suwawa ini menggunakan
                angka, sesuai lokasi yang digali oleh para penambang. Di tiap titik
                bor, penambang akan membangun semacam rumah yang terbuat dari papan
                dan kayu dengan beralaskan terpal. Rumah-rumah itu akan dijadikan
                tempat untuk peristirahatan para penambang dan untuk pengolahan
                hasil tambang. "Jadi di sini sudah semacam perkampungan, kalau sudah
                melihat ada rumah-rumah yang terbangun berarti itu ada aktivitas
                tambangnya atau titik bor," tuturnya. <br />
