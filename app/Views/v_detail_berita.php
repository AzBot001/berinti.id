                <b>BERINTI.ID</b>, Gorontalo - Delapan titik bor aktif itu memiliki
                nama sesuai dengan jarak tempuh kejauhan lokasi tambang. Namanya
                adalah Titik Bor 1, 3, 9, 15, 17, 18, 19, dan Gergaji.
                <br /><br />Lokasi tambang ini diidentifikasi berdasarkan penamaan
                titik bor dengan angka, yang menunjukkan lokasi penggalian. Menurut
                pengakuan seorang penambang, Leon Nasir, bahwa gunung emas di desa
                tersebut sejatinya memiliki 9 titik bor. Namun, 1 titik bor itu
                sudah tak digunakan lagi. Sebab, tak ada emas didalamnya. "Kalau
                dihitung sebenarnya ada 9 titik bor, tapi yang satu itu sudah tak
                berfungsi lagi, karena tidak ada emas di dalamnya," ungkap Leon,
                Kamis (11/7/2024) pagi hari di lokasi. <br />
