                <br />
                Kemudian, untuk menuju ke titik bor 1 sampai ke titik bor lainnya
                membutuhkan yang cukup lama, bahkan bisa berjam-jam untuk
                mencapainya. Seperti halnya dari titik bor 17 menuju ke titik bor 1,
